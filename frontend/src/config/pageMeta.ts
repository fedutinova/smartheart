/**
 * Получить мета-информацию для страницы или использовать дефолты
 */
export function getPageMeta(pageName: string): PageMetaConfig {
  return pageMeta[pageName] || pageMeta.home;
}

/**
 * Получить полный URL страницы
 */
export function getPageUrl(path: string): string {
  const baseUrl = 'https://smartheart.online';
  return `${baseUrl}${path}`;
}
